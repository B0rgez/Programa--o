// Faça um programa que leia dois numeros e imprima-o

#include<stdio.h>
int main(){

    int num1, num2;

    printf("Informe o 1º numero: \n");
    scanf("%d", &num1);

    printf("Informe o 2º numero: \n");
    scanf("%d", &num2);

    printf("Os numeros informados sao: %d e %d", num1, num2);

    return 0;
}

